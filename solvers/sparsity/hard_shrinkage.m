function [ B, W ] = hard_shrinkage( A, W, lam )

%==================================================================================================

if ( lam < 0 )
    
  warning( 'must use positive real thresholding parameter, doing nothing' ); 
  %lam = 0; 
  return
  
end

%==================================================================================================

%abs_A = abs( A );

W = ( W > lam );

B = A .* W;  

%==================================================================================================
